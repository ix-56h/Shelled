int	ft_count_words(const char *s, char c)
{
	int cunt;

	cunt = 1;
	if (!s)
		return (0);
	while (*s)
	{
		while (*s && *s != c)
			s++;
		if (*s == c)
		{
			cunt++;
			s++;
		}
	}
	return (cunt);
}
